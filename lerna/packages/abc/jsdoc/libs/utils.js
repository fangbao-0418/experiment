/**
 * 计算和
 * @param {number} a - 参数1
 * @param {number} b - 参数2
 * @returns {number} - 返回和
 */
function sum (a, b) {
  return a + b
}


/**
 * 比较大小
 * @param {number} a - 参数1
 * @param {number} b - 参数2
 * @returns {number} - 返回最大值
 */
function getMaxNumber (a, b) {
  return a > b ? a : b
}